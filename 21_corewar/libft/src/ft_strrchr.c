#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char		byte;
	const char	*result;

	result = NULL;
	byte = (char)(c % 256);
	while (*s)
	{
		if (*s == byte)
			result = s;
		s++;
	}
	if (*s == byte)
		result = s;
	return ((char *)result);
}
